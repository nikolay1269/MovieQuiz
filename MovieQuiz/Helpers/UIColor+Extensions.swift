    static var ypGreen: UIColor { return UIColor(named: "YP Green") ?? UIColor.green }
    static var ypRed: UIColor { return UIColor(named: "YP Red") ?? UIColor.red }
    static var ypBackground: UIColor { return UIColor(named: "YP Background") ?? UIColor.darkGray}
    static var ypBlack: UIColor { return UIColor(named: "YP Black") ?? UIColor.black }
    static var ypGray: UIColor { return UIColor(named: "YP Gray") ?? UIColor.gray }
    static var ypWhite: UIColor { return UIColor(named: "YP White") ?? UIColor.white }
}
